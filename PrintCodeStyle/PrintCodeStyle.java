public class PrintCodeStyle {
	public static void main(String[] args) {
		// 2 ways to print output to the screen
		
		// Line
		System.out.println("I am a line...");
		// System.out.print("I am a line...\n");
		System.out.println("[MARKER]");
		
		// Not a line
		System.out.print("I am not a line....");
		System.out.println("[MARKER]");
		
		System.out.print("I am a line again...\n");
		System.out.println("[MARKER]");
		
		System.out.print("RILEY");
		System.out.println();
		System.out.println("[MARKER]");

	}
}