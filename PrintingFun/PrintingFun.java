public class PrintingFun {
	public static void main(String[] args) {
		System.out.println(1 + 1);
		// 23
		System.out.println("2" + 3);
		// 2 3
		System.out.println("2 " + 3);
		System.out.println("2" + " " + 3);
		System.out.println(1 + "3" + "3" + 7);
		System.out.println(1 + 3 + "3" + "7");
	}
}